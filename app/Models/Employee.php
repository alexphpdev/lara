	function __construct($depth = 0)
	{

		global $names, $patronymics, $positions;

		$this->depth = $depth;
		$this->f = self::$surnames[array_rand(self::$surnames)];
		$this->i = self::$names[array_rand(self::$names)];
		$this->o = self::$patronymics[array_rand(self::$patronymics)];

		// генерирует опыт сотрудников от 0 дней до 15 лет
		$time = time() - rand(0, 473040000); // 15 лет ( 15 * 365 * 24 * 60 * 60 )
		// устанавливает время на начало дня
		$this->hiring_date = strtotime(date('d-m-Y', $time));

		$this->position = self::$positions[array_rand(self::$positions)];
		$this->salary = rand(10000, 50000);
		$this->id = self::$counter++;
	}

	public function getFIO(){
		return $this->f .' '. $this->i .' '. $this->o;
	}

	public function getF() {return $this->f; }
	public function getI() {return $this->i; }
	public function getO() {return $this->o; }

	public function getId() {
		return $this->id;
	}

	public function __toString(){
		return $this->getId() . '   ' . $this->getFIO() . '   ' . $this->getHiringDateFormated() . '   ' . $this->getPosition() . '   ' . $this->getSalary() . '<br>';
	}

	public function setBossId($id){
		$this->bossId = $id;
	}

	public function setSubId($id) {
		$this->subordinates[] = $id;
		$this->countSubs++;
	}

	public function getCountSubs(){ return $this->countSubs; }

	public function getSubordinates() { return $this->subordinates; }

	public function getDepth() { return $this->depth; }
	
	public function getHiringDate() { return $this->hiring_date; }
	
	public function getHiringDateFormated() { return date('d-m-Y', $this->hiring_date); }

	public function getPosition() { return $this->position; }

	public function getSalary() { return $this->salary; }

	public function getBossId() { return $this->bossId; }

